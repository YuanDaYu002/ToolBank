int fmp4_record(int argc, char* argv[])
{   
    JOSEPH_ACC_CONFIG *joseph_aac_config = NULL;
    JOSEPH_MP4_CONFIG *joseph_mp4_config = NULL;
     
    int nRet = 0;
    int nTmp = 0;
    int nCount = 0; //在joseph_aac_config->pbPCMBuffer已写入的数据长度（g711解码后的数据，直接用于aac编码）
    int nStatus = 0;
    int PCMSize = 320;
    int nPCMRead = 0;
    int nVideoRet = 0;  //这玩意多余啊
    int gBytesRead = 0;
    int nVideoSize = 0;
    int nPCMBufferSize = 0; //g711解码后的数据-->PCM的存储空间，用于直接编码成aac格式 

    printf("fmp4 into 00001\n");
    //unsigned char nVideoBuffer[1048576] = {0}; //栈区1M太大
    unsigned char *nVideoBuffer = calloc(VIDEO_BUFFER_SIZE,1);

    printf("fmp4 into 00002\n");
    unsigned char *pPCM = NULL;
    unsigned char *pbG711ABuffer = NULL;
    unsigned char *pbPCMTmpBuffer = NULL;
    
    pbG711ABuffer = (unsigned char *)malloc(164 *sizeof(unsigned char));
    memset(pbG711ABuffer, 0, 164);  
    pbPCMTmpBuffer = (unsigned char *)malloc(PCMSize *sizeof(unsigned char));
    memset(pbPCMTmpBuffer, 0, PCMSize);
    printf("fmp4 into 00003\n");
      
    /*init aac */
    if((joseph_aac_config=InitAccEncoder()) == NULL)
    {
        printf("init aac failed\n");
        return -1;
    }
    printf("into fmp4 001\n");
    

    /*init mp4*/
    if((joseph_mp4_config=InitMp4Encoder(joseph_aac_config)) == NULL)
    {
        printf("init mp4 failed\n");
        return -1;
    }   
    
    printf("into fmp4 002\n");


    nPCMBufferSize = (joseph_aac_config->nInputSamples*(joseph_aac_config->nPCMBitSize/8));//单位时长（可理解为1S）aac数据所占用内存空间
    /*
    joseph_aac_config->nPCMBitSize/8 : 每个采样（16 bit的位宽）所占字节数
    nInputSamples : 输入的样本数（采样率）,网络传输按照1024的采样率来编码
    */
    printf("nPCMBufferSize = %d\n",nPCMBufferSize);//2048

     
    /*write video audio frame*/
    while(((gBytesRead = fread(pbG711ABuffer, 1, 164, joseph_aac_config->fpIn)) >0) && (nVideoRet < 312))
    {
        nStatus = 0;

        printf("into  fmp4_record while !\n");
        /*hisi audio the first 4 bytes is error*/
        pPCM = pbG711ABuffer + 4;
        memset(pbPCMTmpBuffer, 0, PCMSize);
        memset(joseph_aac_config->pbAACBuffer, 0, joseph_aac_config->nMaxOutputBytes);
         
        /*g711 to pcm*/
        if((nPCMRead = g711a_decode(pbPCMTmpBuffer,  &PCMSize,  pPCM,  gBytesRead-4)) < 0)
        {
            printf(" G711A -> PCM  fail\n");
            break;
        }

        printf("into  fmp4_record while 001 !\n");
        
        /*pcm to aac ; nCount: nCount是已经写入到 joseph_aac_config->pbPCMBuffer 的数据长度*/
        if((nPCMBufferSize - nCount) < nPCMRead)//剩余空间已经不足以放入一帧解码后的PCM数据了
        {

             printf("into  fmp4_record while 002 !\n");
            nStatus = 1;
            memcpy((joseph_aac_config->pbPCMBuffer + nCount), pbPCMTmpBuffer, (nPCMBufferSize - nCount));
            //重新计算写入数据的采样点个数
            joseph_aac_config->nInputSamples = (nPCMBufferSize / (joseph_aac_config->nPCMBitSize / 8));
            nRet = faacEncEncode(joseph_aac_config->hEncoder, (int*) (joseph_aac_config->pbPCMBuffer), \
                                    joseph_aac_config->nInputSamples, joseph_aac_config->pbAACBuffer, joseph_aac_config->nMaxOutputBytes);
            
            printf("into  fmp4_record while 003 !\n");
#if 1                               
            /*get video frame*/
            nVideoSize = joseph_get_H264_frame(joseph_mp4_config, nVideoBuffer, nVideoRet);
            if(nVideoSize < 0)
            {
                printf("read g711a frame failed\n");
                break;
            }
            nVideoRet++;

            printf("into  fmp4_record while 004 !\n");
            /*write video frame to mp4 修改此处视频数据编码成MP4的地方*/
            if((Mp4VEncode(joseph_mp4_config, nVideoBuffer, nVideoSize)) < 0)
            {
                printf("write video frame failed\n");
                break;
            }
#endif
#if FMP4_AUDIO_FLAG                       
            /*write audio frame to mp4*/
            printf("into  fmp4_record while 005 !\n");
            if((Mp4AEncode(joseph_mp4_config, joseph_aac_config->pbAACBuffer, nRet)) < 0)
            {
                printf("write audio frame failed\n");
                break;
            }
#endif          
            printf("into  fmp4_record while 006 !\n");
            /*Treatment of redundant frames 一次读到nPCMRead字节的数据，  处理没完全写入到“oseph_aac_config->pbPCMBuffer”中的数据*/
            nTmp = (nPCMRead - (nPCMBufferSize - nCount));
            /*nCount : 已经写入到oseph_aac_config->pbPCMBuffer中的数据长度
                nPCMBufferSize - nCount : PCMBuffer中剩余的空间大小
                nTmp = nPCMRead - (nPCMBufferSize - nCount) ： 每次读取的数据写满PCMBuffer后还剩下多少写不下的
            */
            
            memset(joseph_aac_config->pbPCMBuffer, 0, nPCMBufferSize);
            memcpy(joseph_aac_config->pbPCMBuffer, (pbPCMTmpBuffer + (nPCMBufferSize-nCount)), nTmp);
            /*
                nPCMBufferSize-nCount : nCount是已经写入的数据长度，
            */
            nCount = 0;
            nCount += nTmp;
             
        }
        
        if( nStatus == 0)
        {
            memcpy(joseph_aac_config->pbPCMBuffer + nCount, pbPCMTmpBuffer, nPCMRead);//nPCMRead 每次返回320字节长度，pbPCMTmpBuffer也是320字节长
            nCount += nPCMRead; 
        }
        
        if(nPCMRead < 320)//g711解码到pcm的数据不满1帧的特殊情况(文件最后读到的一帧可能不满320字节数据)？？？？
        {
            printf("into  fmp4_record while 007 !\n");
            joseph_aac_config->nInputSamples = (nCount / (joseph_aac_config->nPCMBitSize / 8));
            nRet = faacEncEncode(joseph_aac_config->hEncoder, (int*) (joseph_aac_config->pbPCMBuffer), \
                                    joseph_aac_config->nInputSamples, joseph_aac_config->pbAACBuffer, joseph_aac_config->nMaxOutputBytes);
#if 1           
            printf("into  fmp4_record while 008 !\n");
            /*get video frame*/
            nVideoSize = joseph_get_H264_frame(joseph_mp4_config, nVideoBuffer, nVideoRet);
            if(nVideoSize < 0)
            {
                printf("read g711a frame failed\n");
                break;
            }
            nVideoRet++;
             
            /*write video frame to mp4*/
            printf("into  fmp4_record while 009 !\n");
            if((Mp4VEncode(joseph_mp4_config, nVideoBuffer, nVideoSize)) < 0)
            {
                printf("write video frame failed\n");
                break;
            }
#endif
#if 1
            printf("into  fmp4_record while 010 !\n");
            /*write audio frame to mp4*/
            if((Mp4AEncode(joseph_mp4_config, joseph_aac_config->pbAACBuffer, nRet)) < 0)
            {
                printf("write audio frame failed\n");
                break;
            }
#endif          
        }
         
        memset(pbG711ABuffer, 0, 164);  
    }

    printf("into  fmp4_record while 011 !\n");
    /*Close FAAC engine*/
    CloseAccEncoder(joseph_aac_config);
    CloseMp4Encoder(joseph_mp4_config);
     
    /*close file fp*/
    fclose(joseph_aac_config->fpIn);
     
    /*free the source of malloc*/
    //audio part
    free(joseph_aac_config->pbPCMBuffer);
    joseph_aac_config->pbPCMBuffer = NULL;
    free(joseph_aac_config->pbAACBuffer);
    joseph_aac_config->pbAACBuffer = NULL;
    free(pbG711ABuffer);
    pbG711ABuffer = NULL;
    free(pbPCMTmpBuffer);
    pbPCMTmpBuffer = NULL;
    free(joseph_aac_config);
    joseph_aac_config = NULL;
    //video part
    free(joseph_mp4_config);
    joseph_mp4_config = NULL;
	
	free(nVideoBuffer);
 
    printf("%s:[%d] The sys changover succeed !\n",__FUNCTION__,__LINE__);  
    return 0;
}
























